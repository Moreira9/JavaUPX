    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Motorista motorista;
        if (camposPreenchidos()) {

            MororistaDao as = new MororistaDao();
            this.servico = new MotoristaServico();
            String assentosTexto = txtAsssentosVan.getText();

// Convertendo o valor de texto para inteiro
            int assentos = Integer.parseInt(assentosTexto);
            String valorMedioCobradoTexto = txtValorMedioCobrado.getText();

// Tentando converter o valor de texto para double
            double valorMedioCobrado;

            try {
                valorMedioCobrado = Double.parseDouble(valorMedioCobradoTexto);
            } catch (NumberFormatException e) {
                // Lidando com o caso em que o valor não é um número válido
                // Aqui você pode exibir uma mensagem de erro, definir um valor padrão, etc.
                System.out.println("Erro ao converter o valor médio cobrado para double. Usando valor padrão.");
                valorMedioCobrado = 0.0; // Defina um valor padrão ou trate de acordo com a sua lógica
            }

            motorista = new Motorista(this.codMorista, txtCnh.getText(), txtModeloVan.getText(), txtPlaca.getText(), assentos, valorMedioCobrado, as.maiorValor(cadastro));
            servico.inserirMotorista(motorista);
            new CadastroFinalMotorista().setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, preencha todos os campos.", "Campos obrigatórios", JOptionPane.WARNING_MESSAGE);

        }
// TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

