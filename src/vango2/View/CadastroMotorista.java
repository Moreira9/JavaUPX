    public CadastroMotorista(int cod) {
                                                           
        if(this.codMorista == 0){
            this.setTitle("Cadastro de Aluno");
        }else{
            this.setTitle("Alteração de Aluno");
            var a = servico.consultarMotorista(this.codMorista);
            txtCnh.setText(a.getCnh());
            txtModeloVan.setText(a.getModeloVan());
            txtAsssentosVan.setText(String.valueOf(a.getAssentos()));
            txtValorMedioCobrado.setText(String.valueOf(a.getValorAcobrar()));
            
        }
        
        setLocationRelativeTo(null);

    }

